public class Arma {
    private String nome;
    private int dano;


    //GETTERS da arma
    public String getNome() {
        return nome;
    }

    public int getDano() {
        return dano;
    }


    //SETTERS da arma
    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setDano(int dano) {
        this.dano = dano;
    }
}
